function makeReefSpecies(kind: ReefKind, config: ReefSpeciesConfig): SpeciesSeed {
  const isSnapper = kind === "snapper";
  const beginner = isSnapper
    ? `Simple start: ${config.rod}, ${config.reel}, ${config.leader}, and ${config.bestBait}. Fish the easiest public structure first and keep the bait natural.`
    : `Entry point: fish a charter, party boat, or known public reef with ${config.bestBait}. Use stout tackle and practice gaining line immediately after the bite.`;
  const budget = isSnapper
    ? `One versatile spinning setup, a small hook box, fluorocarbon from 20 to 40 lb, and fresh bait cover most ${config.commonName.toLowerCase()} trips.`
    : `A single heavy bottom combo, knocker/fish-finder rigs, and a marked reef list will catch ${config.commonName.toLowerCase()} when conditions and seasons line up.`;
  const serious = isSnapper
    ? `Build a chum-and-flatline program: anchor up-current, start light, feed unweighted baits naturally, and adjust leader size until the larger fish commit.`
    : `The program: sonar homework for low-pressure bottom, spot-lock/precise anchoring, live bait, high drag, and descending gear ready before the first drop.`;

  return {
    id: config.id,
    slug: config.id,
    commonName: config.commonName,
    scientificName: config.scientificName,
    water: "saltwater",
    category: config.category,
    difficulty: config.difficulty,
    beginnerFriendly: config.beginnerFriendly,
    wikiTitle: config.wikiTitle,
    description: config.description,
    avgSize: config.avgSize,
    trophySize: config.trophySize,
    regions: config.regions,
    states: config.states,
    environments: config.environments,
    styles: config.styles,
    seasons: config.seasons,
    baitTypes: config.baitTypes,
    lookalikes: config.lookalikes,
    guide: {
      summary: config.summary,
      quickPlan: {
        bestBaitNow: config.bestBait,
        lureType: config.lureType,
        setup: config.setup,
        locationType: config.locationType,
        bestTime: config.bestTime,
        seasonalNote: config.seasonalNote,
      },
      gear: {
        rod: config.rod,
        reel: config.reel,
        mainLine: config.mainLine,
        leader: config.leader,
        hooks: config.hooks,
        jigheads: config.jigheads,
        terminal: config.terminal,
        lureSizes: config.lureSizes,
        lureColors: config.lureColors,
        baits: config.baits,
        setups: { beginner, budget, serious },
      },
      techniques: {
        presentation: config.presentation,
        retrieve: config.retrieve,
        positioning: config.positioning,
        depth: config.depthRange,
        structure: config.structure,
        current: config.current,
        byStyle: config.byStyle,
      },
      timing: config.timing,
      habitat: {
        overview: config.habitatOverview,
        depthRange: config.depthRange,
        structures: config.habitatStructures,
        lookFor: config.lookFor,
        migration: config.migration,
      },
      mistakes: config.mistakes,
      handling: config.handling,
    },
  };
}

